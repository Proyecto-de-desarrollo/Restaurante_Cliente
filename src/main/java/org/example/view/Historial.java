    public void llenarTabla() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Mesa", "Descripcion"});
        for (int i = 0; i < pc.listarPedidos(user).size(); i++) {
            model.addRow(new Object[]{
                pc.listarPedidos(user).get(i).getMesa().getNumero(),
                pc.listarPedidos(user).get(i).getDescripcion()
            });
        }
        jTable1.setModel(model);
    }
